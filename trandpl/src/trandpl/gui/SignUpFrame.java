    private boolean validateInputs()
     {
         if(txtName.getText().trim().isEmpty()||txtEmail.getText().trim().isEmpty()||txtMobile.getText().trim().isEmpty()||txtFilePath.getText().trim().isEmpty()||txtSkill1.getText().trim().isEmpty()||txtSkill2.getText().trim().isEmpty()||txtSkill3.getText().trim().isEmpty()||txtSkill4.getText().trim().isEmpty()||txtQualification.getText().trim().isEmpty()||String.valueOf(pwdPassword.getPassword()).isEmpty()||String.valueOf(pwdCPassword.getPassword()).isEmpty())
             return false;
         else
             return true;
     }
     private boolean pwdMatch()
     {
         if(String.valueOf(pwdPassword.getPassword()).equals(String.valueOf(pwdCPassword.getPassword())))
             return true;
         else
             return false;
     }
