       private boolean validateInputs()
       {
           if(listUserId.getSelectedIndex()==0||String.valueOf(txtPassword.getPassword()).isEmpty()||String.valueOf(txtConfirmPassword.getPassword()).isEmpty())
               return false;
           else 
               return true;
       }
       
       private boolean matchPassword()
       {
           if(String.valueOf(txtPassword.getPassword()).equals(String.valueOf(txtConfirmPassword.getPassword())))
                   return true;
           else
               return false;
       }
       private void clearFields()
       {
           listUserId.setSelectedIndex(0);
           txtHrId.setText("");
           lblHrName.setText("");
           txtPassword.setText("");
           txtConfirmPassword.setText("");
       }
