    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       boolean result=validateInputs();
       if(result==false)
       {
           JOptionPane.showMessageDialog(this,"Field should not be left blank","Error !",JOptionPane.ERROR_MESSAGE);
           return ;
       }
       result=matchPassword();
       if(result==false)
       {
           JOptionPane.showMessageDialog(this,"Passoword do not match ","Error",JOptionPane.ERROR_MESSAGE);
           return ;
       }
       if(txtPassword.getPassword().length<=4)
       {
           JOptionPane.showMessageDialog(this,"Password atleast of more than 4 character","Error !",JOptionPane.ERROR_MESSAGE);
           return ;
       }
       try{
           String password=String.valueOf(txtPassword.getPassword());
           String userId=listUserId.getSelectedItem().toString();
           result=UsersDAO.changePassword(userId, password);
           if(result==true)
            {
              JOptionPane.showMessageDialog(this,"Hr Modified","Success",JOptionPane.INFORMATION_MESSAGE);
              clearFields();
              return ;
            }
           else
            {
              JOptionPane.showMessageDialog(this,"Hr can't modified ","Error",JOptionPane.ERROR_MESSAGE);
              return ;
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(this,"DB Error ","DB Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

