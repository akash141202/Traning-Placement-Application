    private void btnPreviousActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPreviousActionPerformed
     if(validateInputs()==false)
     {
         JOptionPane.showMessageDialog(null,"Please fill all the fields","Warning ",JOptionPane.WARNING_MESSAGE);
         return ;
     }
       queRef=getQuestion();
       questions.set(counter, queRef); 
        if(counter==0)
           counter=this.questions.size()-1;
       else 
           counter--;
       
       queRef=this.questions.get(counter);
       setQuestion(queRef); 
    }//GEN-LAST:event_btnPreviousActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
         if(validateInputs()==false)
          {
           JOptionPane.showMessageDialog(null,"Please fill all the fields","Warning ",JOptionPane.WARNING_MESSAGE);
           return ;
          }
          queRef=getQuestion();
         questions.set(counter, queRef); 
        questions.set(counter, queRef);
        if(counter==this.questions.size()-1)
             counter=0;
         else
             counter++;
         
         queRef=this.questions.get(counter);
         setQuestion(queRef);
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnDiscardPaperActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDiscardPaperActionPerformed
        int result=JOptionPane.showConfirmDialog(null,"Are you sure, Do you want to Discard paper ","Confrimation",JOptionPane.YES_NO_OPTION);
        if(result==JOptionPane.YES_OPTION)
        {
            questions.clear();
            new HrChooseJobForSettingQuizz().setVisible(true);
            this.dispose();
        }

    }//GEN-LAST:event_btnDiscardPaperActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
       if(validateInputs()==false)
       {
           JOptionPane.showMessageDialog(null,"Please fill all the fields","Warning ",JOptionPane.WARNING_MESSAGE);
           return ; 
       }
        questions.set(counter, queRef);
       try{
           boolean result=QuestionDAO.updateQuestion(questions);
           if(result==true)
           {
               JOptionPane.showMessageDialog(null,"Paper Succssfully updated","Success",JOptionPane.INFORMATION_MESSAGE);
               new HrQuizOptionFrame().setVisible(true);
               this.dispose();
           }
           else{
               JOptionPane.showMessageDialog(null,"Paper could not be updated","Error !",JOptionPane.ERROR_MESSAGE);
           }
       }
       catch(SQLException ex)
       {
           JOptionPane.showMessageDialog(null,"Error in Db","Error !",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
       }
         
    }//GEN-LAST:event_btnSubmitActionPerformed

