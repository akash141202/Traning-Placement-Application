    private void loadQuestions()
    {
       try{
           this.questions=QuestionDAO.getPaperByJobId(jobId);
           this.queRef=questions.get(counter);
           setQuestion(queRef);
       } 
       catch(SQLException ex)
       {
           JOptionPane.showMessageDialog(null,"Error in DB","Error !",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
       }
    }
    private void setQuestion(QuestionPojo obj)
    {
        lblQueNo.setText(String.valueOf(counter+1));
        txtQuestion.setText(obj.getQuestion());
        txtOption1.setText(obj.getOption1());
        txtOption2.setText(obj.getOption2());
        txtOption3.setText(obj.getOption3());
        txtOption4.setText(obj.getOption4());
        jcCorrectOption.setSelectedIndex(obj.getCorrectOption());
    }
    private QuestionPojo getQuestion()
    {
        QuestionPojo que=new QuestionPojo();
        que.setJobId(jobId);
        que.setqNo(Integer.parseInt(lblQueNo.getText()));
        que.setQuestion(txtQuestion.getText());
        que.setOption1(txtOption1.getText());
        que.setOption2(txtOption2.getText());
        que.setOption3(txtOption3.getText());
        que.setOption4(txtOption4.getText());
        que.setCorrectOption(jcCorrectOption.getSelectedIndex());
        return que;
    }
      private boolean validateInputs()
    {
        if(txtQuestion.getText().trim().isEmpty()||txtOption1.getText().trim().isEmpty()||txtOption2.getText().trim().isEmpty()||txtOption3.getText().trim().isEmpty()||txtOption4.getText().trim().isEmpty()||jcCorrectOption.getSelectedIndex()==0)
            return false;
        else
            return true;
    }

